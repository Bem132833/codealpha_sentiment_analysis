"""
cross_domain_eval.py
----------------------
The core analytical contribution of this project: rather than only
reporting one accuracy number, this module measures how well a sentiment
model trained on ONE domain (e.g. product reviews) generalizes to a
DIFFERENT domain (e.g. tweets or financial news).

Sentiment analysis models are notoriously domain-brittle — a classifier
trained on star-rating reviews often collapses on short, sarcastic tweets,
and struggles even more on formal, understatement-heavy financial text.
This module produces a 3x3 "train-on-row, test-on-column" macro-F1 matrix
that makes that effect visible, then compares it against the combined
multi-domain model to show whether training on all three domains together
recovers the generalization gap.

Output: outputs/metrics/cross_domain_metrics.json and a heatmap figure
generated by visualization.py from that JSON.
"""

import pandas as pd
from sklearn.feature_extraction.text import TfidfVectorizer
from sklearn.linear_model import LogisticRegression
from sklearn.metrics import f1_score
from sklearn.model_selection import train_test_split

from src import config
from src.utils import get_logger, save_json, timeit

logger = get_logger(__name__)


def _train_single_domain_model(train_texts, train_labels):
    vectorizer = TfidfVectorizer(
        max_features=config.TFIDF_MAX_FEATURES,
        ngram_range=config.TFIDF_NGRAM_RANGE,
        min_df=1,  # smaller per-domain data, so relax min_df
        max_df=config.TFIDF_MAX_DF,
        sublinear_tf=True,
    )
    X = vectorizer.fit_transform(train_texts)
    model = LogisticRegression(**config.MODEL_CANDIDATES["logistic_regression"])
    model.fit(X, train_labels)
    return model, vectorizer


@timeit
def run_cross_domain_matrix(df: pd.DataFrame) -> dict:
    """
    For every domain pair (source, target):
      - train a fresh TF-IDF + LogisticRegression model on source domain's
        training split only
      - evaluate it on target domain's test split
      - record macro-F1

    Diagonal entries (source == target) are the "in-domain" baseline.
    Off-diagonal entries reveal the generalization gap.
    """
    domains = [d for d in config.DOMAINS if (df["domain"] == d).any()]

    # Build a per-domain train/test split once, reused for every pairing.
    domain_splits = {}
    for domain in domains:
        sub = df[df["domain"] == domain]
        if sub["label"].nunique() < 2 or len(sub) < 10:
            logger.warning(f"Skipping '{domain}': not enough data/classes for cross-domain eval")
            continue
        train_sub, test_sub = train_test_split(
            sub, test_size=config.TEST_SIZE, random_state=config.RANDOM_STATE,
            stratify=sub["label"],
        )
        domain_splits[domain] = (train_sub, test_sub)

    valid_domains = list(domain_splits.keys())
    matrix = {src: {} for src in valid_domains}

    for source in valid_domains:
        train_sub, _ = domain_splits[source]
        model, vectorizer = _train_single_domain_model(train_sub["clean_text"], train_sub["label"])

        for target in valid_domains:
            _, test_sub = domain_splits[target]
            X_target = vectorizer.transform(test_sub["clean_text"])
            preds = model.predict(X_target)
            macro_f1 = f1_score(test_sub["label"], preds, average="macro", zero_division=0)
            matrix[source][target] = round(float(macro_f1), 4)
            tag = "IN-DOMAIN" if source == target else "cross-domain"
            logger.info(f"train={source:<22} test={target:<22} macro-F1={macro_f1:.4f} [{tag}]")

    # Also train one combined model on all domains pooled together, and
    # test it on each domain's held-out test split, for comparison.
    combined_train = pd.concat([domain_splits[d][0] for d in valid_domains])
    combined_model, combined_vectorizer = _train_single_domain_model(
        combined_train["clean_text"], combined_train["label"]
    )
    combined_scores = {}
    for target in valid_domains:
        _, test_sub = domain_splits[target]
        X_target = combined_vectorizer.transform(test_sub["clean_text"])
        preds = combined_model.predict(X_target)
        macro_f1 = f1_score(test_sub["label"], preds, average="macro", zero_division=0)
        combined_scores[target] = round(float(macro_f1), 4)
        logger.info(f"train=ALL_DOMAINS_COMBINED   test={target:<22} macro-F1={macro_f1:.4f}")

    result = {
        "matrix": matrix,
        "combined_model_scores": combined_scores,
        "domains_evaluated": valid_domains,
        "note": (
            "matrix[source][target] = macro-F1 when training on `source` "
            "and testing on `target`. Diagonal = in-domain baseline. "
            "combined_model_scores = macro-F1 when training on all domains "
            "pooled together, evaluated per domain."
        ),
    }

    save_json(result, config.CROSS_DOMAIN_METRICS_FILE)
    logger.info(f"Saved cross-domain matrix to {config.CROSS_DOMAIN_METRICS_FILE}")
    return result


if __name__ == "__main__":
    if not config.PROCESSED_FILE.exists():
        logger.error(
            f"{config.PROCESSED_FILE} not found. Run data_loader.py then "
            f"preprocessing.py first."
        )
    else:
        data = pd.read_csv(config.PROCESSED_FILE)
        run_cross_domain_matrix(data)
